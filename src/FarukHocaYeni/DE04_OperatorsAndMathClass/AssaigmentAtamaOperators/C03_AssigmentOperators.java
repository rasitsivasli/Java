package DE04_OperatorsAndMathClass.AssaigmentAtamaOperators;

public class C03_AssigmentOperators {
    public static void main(String[] args) {
        int sayi=10;

        sayi += 5;   // sayi = sayi + 5; // 15
        sayi -= 3;   // sayi = sayi - 3; // 12
        sayi *= 2;   // sayi = sayi * 2; // 24
        sayi /= 2;   // sayi = sayi / 2; // 12
        System.out.println(sayi);
    }
}
