package _____OSA_____.DE40InterviewQuestions.Day04;

import java.util.Scanner;

public class Q04_PrintVowelsOfString {
    //Get String from user and print just vowels(aeiou) inside String on the console

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        System.out.print("Bir string giriniz: ");
        String str = scanner.nextLine();
        String sesliHarfler = str.replaceAll("[^aeiouAEIOU]", "");
        System.out.println("sesliHarfler = " + sesliHarfler);

    }
}
