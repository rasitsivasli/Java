package Privat.DE07lik7DersPekistirmeSorulari;

public class B03 {
    public static void main(String[] args) {
        // Lütfen kodu çalıştırmadan, her bir satır sonunda
        // variable ların alacağı değeri (soru işareti yerine) yazınız
        int sayi = 5;
        sayi = sayi + 2 ;           // sayi = 7
        System.out.println("sayi = " + sayi);
        sayi -= 5;                  // sayi = 2
        System.out.println("sayi = " + sayi);
        sayi++;                     // sayi = 3
        System.out.println("sayi = " + sayi);
        sayi = sayi / 2 ;           // sayi = 1
        System.out.println("sayi = " + sayi);
        sayi = sayi * 2 ;           // sayi = 2
        System.out.println("sayi = " + sayi);
        sayi = sayi % 3 + 1;        // sayi = 3
        System.out.println("sayi = " + sayi);
        sayi *= 5 ;                 // sayi = 15
        System.out.println("sayi = " + sayi);
        sayi -= 2 ;                 // sayi = 13
        System.out.println("sayi = " + sayi);
        sayi = sayi / 2 ;           // sayi = 6
        System.out.println("sayi = " + sayi);
        int sayi2 = sayi;           // sayi = 6

        System.out.println("sayi = " + sayi); // sayi  = 1
        System.out.println("sayi2= " + sayi2);// sayi2 = 1

    }
}
